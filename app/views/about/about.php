    <h2 class="aboutTitle">Nuestra Historia</h2>
    <p class="aboutP">La idea surgió una noche de invierno, mientras competíamos en un maratón de juegos de mesa.
        Nos dimos cuenta de que nuestra pequeña ciudad carecía de un lugar donde los aficionados como ellos pudieran
        comprar una variedad de juegos.
        Además, notamos que muchos juegos innovadores y menos conocidos apenas llegaban a las tiendas locales.

        Movidos por nuestro entusiasmo compartido por los juegos y la falta de acceso a ellos, decidimos crear un
        e-commerce de juegos de mesa.
        Queríamos ofrecer no solo los juegos populares, sino también aquellos tesoros ocultos y creaciones
        independientes.
        Sergio aportó su conocimiento en estrategia y logística, Sergio su habilidad para contar historias y
        mercadotecnia
        y Pablo su experiencia en gestión de negocios y servicio al cliente.

        Así nació "Gecomme", un e-commerce que no solo vendía juegos, sino que también construía una comunidad.
        Desde entonces, hemos crecido y queremos ser un referente en el mundo de los juegos de mesa.</p>

    <h2 class="aboutTitle">Misión y Visión</h2>
    <p class="aboutP"><strong>Misión:</strong> Proporcionar juegos de mesa de alta calidad para todas las edades, fomentando la
        diversión y el aprendizaje.</p>
    <p class="aboutP"><strong>Visión:</strong> Ser líderes en el mercado de juegos de mesa, innovando constantemente y expandiendo
        nuestra comunidad de jugadores.</p>

